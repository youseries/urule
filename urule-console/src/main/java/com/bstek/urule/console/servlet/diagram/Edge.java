package com.bstek.urule.console.servlet.diagram;
/**
 * @author Jacky.gao
 * @since 2015年1月6日
 */
public class Edge {
	private int from;
	private int to;
	public Edge(int from, int to) {
		this.from = from;
		this.to = to;
	}
	public int getFrom() {
		return from;
	}
	public void setFrom(int from) {
		this.from = from;
	}
	public int getTo() {
		return to;
	}
	public void setTo(int to) {
		this.to = to;
	}
}
